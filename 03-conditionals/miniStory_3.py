cup = input("Choose your cup size (small, medium, large): ").lower()

if cup == "small":
    print("You chose a small cup. Enjoy your drink!")
elif cup == "medium":
    print("You chose a medium cup. Enjoy your drink!")
elif cup == "large":
    print("You chose a large cup. Enjoy your drink!")
else:
    print("Invalid choice. Please choose small, medium, or large.")
