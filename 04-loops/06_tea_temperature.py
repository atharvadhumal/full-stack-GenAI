temp = 40

while temp <= 100:
    print(f"current temperature: {temp}°C")
    #temp = temp + 15
    temp += 15

print("The tea is ready to drink!")

#explanation : The code initializes a variable `temp` to 40. It then enters a `while` loop that continues as long as `temp` is less than or equal to 100. Inside the loop, it prints the current temperature and increments `temp` by 15. Once the loop condition is no longer met, it prints a message indicating that the tea is ready to drink.